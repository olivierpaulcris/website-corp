          <h2 class="titulo-informacion">Historia</h2>
          <!--<div id ="div_historia" style = "display:none;">-->
          <p><strong>Es en Julio de 1992</strong>, cuando el Lic. Víctor Manuel Robles Payán abre su despacho en una oficina ubicada en 
            la colonia Centro, calle Rafael Buelna #664 Oriente, contando sólo con una secretaria y dos gestores, atendiendo 
            solamente cobranza extrajudicial en tarjetas de crédito para lo que era en ese entonces Multibanco Comermex.</p>

          <p><strong>En 1993</strong> se incorpora, la Lic. Josefina Robles Payán como auxiliar contable y coordinando a los gestores. Al ser 
            ésta una empresa que día a día iba creciendo, se obtuvieron más clientes, incorporándose como uno de ellos Banoro, 
            por lo cual se amplían los servicios con cartera judicial.</p>

          <p><strong>En 1994</strong> el despacho empezó a llevar la cartera de dos bancos, Banoro que después se convirtió en Bancrecer y 
            actualmente Banorte; y Multibanco Comermex, que después cambió su nombre a Inverlat y actualmente es Scotiabank.</p>

          <p><strong>En 1998</strong> se abre la plaza de Mochis en el edificio Barriza departamento 209 llevando solamente cartera extrajudicial, 
            actualmente ésta plaza sigue funcionando estando al frente de ella el Lic. Gehú Verdugo.</p>

          <p><strong>Es en el año 2001</strong> cuando ocurren grandes cambios de manera importante en la empresa, dando un vuelco en la operación, 
            tal es así que nos vimos en la necesidad de crear una oficina más funcional acorde a las nuevas exigencias de la 
            empresa, trasladándose a las instalaciones actuales en calle Colón #834 Ote. Despacho. 6 Colonia Centro, el día 07 de 
            Julio de ese mismo año abren sus puertas al público. Es en el 2001, también, cuando el corporativo crece hacia el sur 
            del estado abriendo una plaza en la ciudad de Mazatlán en calle José Ángel Ferrusquilla Edif. 48-A Desp. 202 fracc. 
            Palos prietos, a cargo del Lic. Cuauhtémoc Salomón Pérez, quien se traslada a dicha ciudad para coordinar la nueva 
            plaza y quien sigue vigente al mando de la misma, en ese momento la plaza de Mazatlán contaba con Banco Mercantil del 
            Norte como único cliente.</p>

          <p><strong>En Junio de 2003</strong> se celebraron los 10 años de la empresa en una fiesta otorgada en la torre académica. Para el año 
            2005, la empresa contaba ya con 19 empleados incluido el personal de plazas. Es en Diciembre de 2007 cuando se 
            celebran los quince años del corporativo.</p>

          <p><strong>El 2007</strong> fue un año muy importante, ya que en Junio se les otorga la cartera de INFONAVIT, para coordinarla se 
            incorpora de lleno el Lic. Juan Manuel Cruz Ortega, quien actualmente funge con el mismo puesto. Una vez más, 
            gracias al desarrollo de la empresa y al incremento en la demanda de sus clientes, el corporativo decide ampliar 
            sus instalaciones abriendo, para inicios del 2008, las oficinas ubicadas en calle Cristóbal Colón #834 Ote. 
            Despacho 1 Col. Centro. </p>

          <p><strong>En el año 2010</strong>, el cliente INFONAVIT otorga al corporativo una autorización especial para operar en una filial que 
            lleva el nombre de C. A. S. A. (Centro de Atención y Solución Autorizada), la cual inició sus labores operativas en 
            Junio del año antes mencionado, manejando cobranza social y bajo supervisión del Lic. Romel Sánchez.</p>

          <p><strong>Esta empresa ha tenido un crecimiento visiblemente importante que se ve reflejado en su plantilla laboral</strong>, 
            la cual en un inicio en el año 1992, contaba con 4 personas y a 20 años de su fundación, se ha extendido de manera 
            considerable a 42 personas en su equipo de trabajo.</p> 
